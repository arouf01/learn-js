// 1. length
let count = "Hey, there!";
console.log(count.length);

// 2. toUpperCase()
let sample = "abcdefgh";
console.log(sample.toUpperCase());

// 3. toLowerCase()
let demo = "AbcdEFGh";
console.log(demo.toLowerCase());

// 4. slice()
let example = "Wait a minute... Who are you ?";
console.log(example.slice(17, 30));
// in slice, we can also use the negative value for getting from the last char
console.log(example.slice(17, -1));

// 5. substring()
let cut = "Ok! Bye...";
console.log(cut.substring(4, 10)); // Can't use the negative value here
